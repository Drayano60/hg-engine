    UpdateVarFromVar OPCODE_SET, BSCRIPT_VAR_MSG_BATTLER_TEMP, BSCRIPT_VAR_BATTLER_DEFENDER
    Call BATTLE_SUBSCRIPT_RECOVER_HP
_End:
    End
